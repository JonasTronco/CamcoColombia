package com.campocolombia.modelo.Clima;

public class Clima {

    int IdClima;
    double TemperaturaClima;
    double HumedadClima;
    double PrecipitacionClima;
    String FechaClima;
    int IdEmpleado;

    public int getIdClima() {
        return IdClima;
    }

    public void setIdClima(int IdClima) {
        this.IdClima = IdClima;
    }

    public double getTemperaturaClima() {
        return TemperaturaClima;
    }

    public void setTemperaturaClima(double TemperaturaClima) {
        this.TemperaturaClima = TemperaturaClima;
    }

    public double getHumedadClima() {
        return HumedadClima;
    }

    public void setHumedadClima(double HumedadClima) {
        this.HumedadClima = HumedadClima;
    }

    public double getPrecipitacionClima() {
        return PrecipitacionClima;
    }

    public void setPrecipitacionClima(double PrecipitacionClima) {
        this.PrecipitacionClima = PrecipitacionClima;
    }

    public String getFechaClima() {
        return FechaClima;
    }

    public void setFechaClima(String FechaClima) {
        this.FechaClima = FechaClima;
    }

    public int getIdEmpleado() {
        return IdEmpleado;
    }

    public void setIdEmpleado(int IdEmpleado) {
        this.IdEmpleado = IdEmpleado;
    }
    
    
}
