package com.campocolombia.modelo.Investigadores;


/**
 *
 * @author Jonattan
 */
public class Investigador {

    int IdRegistroC;
    int IdEmpleado;
    int IdProducto;
    String FechaRegistroC;
    double PuntacionRegistroC;
    String AprovacionRegistroC;

    public int getIdRegistroC() {
        return IdRegistroC;
    }

    public void setIdRegistroC(int IdRegistroC) {
        this.IdRegistroC = IdRegistroC;
    }

    public int getIdEmpleado() {
        return IdEmpleado;
    }

    public void setIdEmpleado(int IdEmpleado) {
        this.IdEmpleado = IdEmpleado;
    }

    public int getIdProducto() {
        return IdProducto;
    }

    public void setIdProducto(int IdProducto) {
        this.IdProducto = IdProducto;
    }

    public String getFechaRegistroC() {
        return FechaRegistroC;
    }

    public void setFechaRegistroC(String FechaRegistroC) {
        this.FechaRegistroC = FechaRegistroC;
    }

    public double getPuntacionRegistroC() {
        return PuntacionRegistroC;
    }

    public void setPuntacionRegistroC(double PuntacionRegistroC) {
        this.PuntacionRegistroC = PuntacionRegistroC;
    }

    public String getAprovacionRegistroC() {
        return AprovacionRegistroC;
    }

    public void setAprovacionRegistroC(String AprovacionRegistroC) {
        this.AprovacionRegistroC = AprovacionRegistroC;
    }

   
    

}
