package com.campocolombia.modelo.gestionabejas;

/**
 *
 * @author Jonattan
 */
public class Abeja {
    
    private int IdAbejas;
    private String NombreAbejas;
    private String RazaAbejas;
    private double PesoAbejas;
    private double AltoAbejas;
    private double colorAbejas;

    public int getIdAbejas() {
        return IdAbejas;
    }

    public void setIdAbejas(int IdAbejas) {
        this.IdAbejas = IdAbejas;
    }

    public String getNombreAbejas() {
        return NombreAbejas;
    }

    public void setNombreAbejas(String NombreAbejas) {
        this.NombreAbejas = NombreAbejas;
    }

    public String getRazaAbejas() {
        return RazaAbejas;
    }

    public void setRazaAbejas(String RazaAbejas) {
        this.RazaAbejas = RazaAbejas;
    }

    public double getPesoAbejas() {
        return PesoAbejas;
    }

    public void setPesoAbejas(double PesoAbejas) {
        this.PesoAbejas = PesoAbejas;
    }

    public double getAltoAbejas() {
        return AltoAbejas;
    }

    public void setAltoAbejas(double AltoAbejas) {
        this.AltoAbejas = AltoAbejas;
    }

    public double getColorAbejas() {
        return colorAbejas;
    }

    public void setColorAbejas(double colorAbejas) {
        this.colorAbejas = colorAbejas;
    }
    
    
    
    
}
